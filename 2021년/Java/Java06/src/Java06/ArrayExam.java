package Java06;



public class ArrayExam {
	public static void main(String[] args) {
		
		int array[] = new int[10]; // 0~9까지의 수를 담음
		for (int i =0; i < array.length; i++) {
			System.out.println(array[i]);
		}
		System.out.println();

		String a[] = {"a","b","c"};
		for (int j = 0; j < a.length; j++) {
			System.out.println(a[j]);
		}
		
		
		
		
	}
}
